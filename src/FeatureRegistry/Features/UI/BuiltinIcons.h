static constexpr int BUILTIN_ICON_COUNT = sizeof(builtinIcons) / sizeof(builtinIcons[0]);

// Find a builtin icon by name at preferred size. Returns nullptr if not found.
inline const IconData *getBuiltinIcon(const char *name, int preferredSize = 16)
{
    for (int i = 0; i < BUILTIN_ICON_COUNT; i++)
    {
        if (strcmp(builtinIcons[i].name, name) == 0)
        {
            if (preferredSize >= 24 && builtinIcons[i].icon32 != nullptr)
                return builtinIcons[i].icon32;
            return builtinIcons[i].icon16;
        }
    }
    return nullptr;
}

// Draw a named builtin icon at position, scaled to targetSize.
// Uses 32x32 source when targetSize >= 24 for better quality.
// Falls back to generic_file if name not found.
inline void drawBuiltinIcon(LGFX_Sprite &canvas, const char *name, int x, int y, int targetSize = 16)
{
    int sourceSize = (targetSize >= 24) ? 32 : 16;
    const IconData *icon = getBuiltinIcon(name, sourceSize);
    if (icon == nullptr)
    {
        icon = getBuiltinIcon("generic_file", sourceSize);
    }
    if (icon == nullptr)
    {
        icon = &iconData16_generic_file;
    }
    drawIndexedIconScaled(canvas, *icon, x, y, targetSize);
}

// Get a default icon name for a startMenu category path
inline const char *getDefaultIconForCategory(const String &startMenu)
{
    if (startMenu.startsWith("/System"))
        return "computer";
    if (startMenu.startsWith("/Settings"))
        return "settings";
    return "generic_file";
}

